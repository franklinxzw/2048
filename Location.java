// Location - class for storing coordinates
public class Location
{
	//Attributes
	private int x;
	private int y;
	
	//Constructor
	public Location(int x, int y)
	{
		this.x = x;
		this.y = y;
	}
	
	//Getter Methods
	public int getX()
	{
		return x;
	}
	
	public int getY()
	{
		return y;
	}
}